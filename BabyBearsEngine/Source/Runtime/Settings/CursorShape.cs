namespace BabyBearsEngine;

public enum CursorShape
{
    /// <summary>The platform default arrow cursor.</summary>
    Default,
    /// <summary>Invisible cursor.</summary>
    Hidden,
    /// <summary>Text input I-beam.</summary>
    IBeam,
    /// <summary>Crosshair.</summary>
    Crosshair,
    /// <summary>Pointing/link hand.</summary>
    PointingHand,
    /// <summary>Four-directional move cursor.</summary>
    ResizeAll,
    /// <summary>East-west resize.</summary>
    ResizeEW,
    /// <summary>North-south resize.</summary>
    ResizeNS,
    /// <summary>North-east / south-west resize.</summary>
    ResizeNESW,
    /// <summary>North-west / south-east resize.</summary>
    ResizeNWSE,
    /// <summary>Not-allowed / forbidden.</summary>
    NotAllowed,
}
